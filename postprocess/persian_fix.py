def improve_persian_text(text: str) -> str:
    if not text:
        return ""

    # --------------------------------------------------
    # نرمال‌سازی اولیه
    # --------------------------------------------------

    text = text.replace("\r\n", "\n").replace("\r", "\n")

    # حذف کاراکترهای نامرئی
    text = re.sub(
        r'[\u200c\u200d\u200e\u200f\u202a-\u202e\u2066-\u2069]',
        '',
        text
    )

    # --------------------------------------------------
    # تبدیل حروف عربی OCR به فارسی
    # --------------------------------------------------

    arabic_to_persian = {
        "ك": "ک",
        "ي": "ی",
        "ى": "ی",
        "ئ": "ی",
        "ؤ": "و",
        "ة": "ه",
        "ۀ": "ه",

        # OCR fixes
        "كی": "کی",
        "مى": "می",
        "نمى": "نمی",

        # عربی به فارسی
        "كافى": "کافی",
        "دنيا": "دنیا",

        # حروف اشتباه OCR
        "ھ": "ه",
    }

    for old, new in arabic_to_persian.items():
        text = text.replace(old, new)

    # --------------------------------------------------
    # اصلاح کلمات چسبیده رایج
    # --------------------------------------------------

    glued_fixes = [
        (r"\bوبه\b", "و به"),
        (r"\bواز\b", "و از"),
        (r"\bودر\b", "و در"),
        (r"\bوگفت\b", "و گفت"),
        (r"\bردشد\b", "رد شد"),
        (r"\bاماکه\b", "اما که"),
        (r"\bازآن\b", "از آن"),
        (r"\bهرروز\b", "هر روز"),
        (r"\bهیچکس\b", "هیچ‌کس"),
        (r"\bهیچکدام\b", "هیچ‌کدام"),
        (r"\bهمدیگر\b", "هم‌دیگر"),
        (r"\bهمیشهگی\b", "همیشگی"),

        # OCR رایج
        (r"\bپنجر\s*5\b", "پنجره"),
        (r"\bپنجر\b", "پنجره"),
        (r"\bپنجر\s*د‌ای\b", "پنجره‌ای"),

        # خاص
        (r"\bلحظههاست\b", "لحظه‌هاست"),
    ]

    for pattern, repl in glued_fixes:
        text = re.sub(pattern, repl, text)

    # --------------------------------------------------
    # اصلاح نیم‌فاصله می / نمی
    # --------------------------------------------------

    text = re.sub(
        r"\bمی\s*([اآ-ی]+)",
        r"می‌\1",
        text
    )

    text = re.sub(
        r"\bنمی\s*([اآ-ی]+)",
        r"نمی‌\1",
        text
    )

    text = re.sub(
        r"\bمى\s*([اآ-ی]+)",
        r"می‌\1",
        text
    )

    text = re.sub(
        r"\bنمى\s*([اآ-ی]+)",
        r"نمی‌\1",
        text
    )

    # --------------------------------------------------
    # جمع فارسی (stable)
    # --------------------------------------------------

    # لذت ها → لذت‌ها
    text = re.sub(
        r"\b([اآ-ی]+)\s+ها\b",
        r"\1‌ها",
        text
    )

    # همسایهها → همسایه‌ها
    # خانهها → خانه‌ها
    text = re.sub(
        r"\b([اآ-ی]+ه)ها\b",
        r"\1‌ها",
        text
    )

    # گنجشکها → گنجشک‌ها
    # فصلها → فصل‌ها
    text = re.sub(
        r"\b([اآ-ی]+[^ه‌\s])ها\b",
        r"\1‌ها",
        text
    )

    # حذف نیم‌فاصلهٔ تکراری
    # همسایه‌‌ها → همسایه‌ها
    text = re.sub(
        r"‌{2,}",
        "‌",
        text
    )

    # پنجرهه → پنجره
    text = re.sub(
        r"([اآ-ی]+)هه\b",
        r"\1ه",
        text
    )

    # پنجرهه‌های → پنجره‌های
    text = re.sub(
        r"هه‌های\b",
        r"ه‌های",
        text
    )
    # وبهکوچه → و به کوچه
    text = re.sub(
        r"\bوبه([اآ-ی]+)",
        r"و به \1",
        text
    )

    # ودرخانه → و در خانه
    text = re.sub(
        r"\bودر([اآ-ی]+)",
        r"و در \1",
        text
    )

    # وازدوست → و از دوست
    text = re.sub(
        r"\bواز([اآ-ی]+)",
        r"و از \1",
        text
    )

    # پسربچهای → پسربچه‌ای
    text = re.sub(
        r"\b([اآ-ی]{3,})های\b",
        r"\1ه‌ای",
        text
    )

    # --------------------------------------------------
    # ترین
    # بزرگترین → بزرگ‌ترین
    # --------------------------------------------------

    text = re.sub(
        r"\b([اآ-ی]{2,})ترین\b",
        r"\1‌ترین",
        text
    )

    # --------------------------------------------------
    # تر
    # بزرگتر → بزرگ‌تر
    # --------------------------------------------------

    text = re.sub(
        r"\b([اآ-ی]{3,})تر\b",
        r"\1‌تر",
        text
    )

    # --------------------------------------------------
    # های فارسی
    # --------------------------------------------------

    # پنجره های → پنجره‌های
    text = re.sub(
        r"\b([اآ-ی]+ه)\s+های\b",
        r"\1‌های",
        text
    )

    # کتاب های → کتاب‌های
    text = re.sub(
        r"\b([اآ-ی]+[^ه\s])\s+های\b",
        r"\1‌های",
        text
    )
    # --------------------------------------------------
    # پسربچه ای → پسربچه‌ای
    # --------------------------------------------------

    text = re.sub(
        r"\b([اآ-ی]{3,})\s+ای\b",
        r"\1‌ای",
        text
    )

    # فکرکرد → فکر کرد
    text = re.sub(
        r"\b([اآ-ی]{2,})کرد\b",
        r"\1 کرد",
        text
    )

    # تمامشد → تمام شد
    text = re.sub(
        r"\b([اآ-ی]{2,})شد\b",
        r"\1 شد",
        text
    )

    # جوابگفت → جواب گفت
    text = re.sub(
        r"\b([اآ-ی]{2,})گفت\b",
        r"\1 گفت",
        text
    )

    # --------------------------------------------------
    # اصلاح برخی OCRهای رایج فارسی
    # --------------------------------------------------

    common_ocr_fixes = {
        "تن‌هاست": "تنهاست",
        "مي": "می",
        "نمي": "نمی",
        "كرد": "کرد",
        "ميشود": "می‌شود",
        "نمیشود": "نمی‌شود",
        "ميكند": "می‌کند",
        "نميكند": "نمی‌کند",
        "ميخواست": "می‌خواست",
        "نميدانست": "نمی‌دانست",
    }

    for old, new in common_ocr_fixes.items():
        text = text.replace(old, new)

    # --------------------------------------------------
    # اصلاح علائم نگارشی
    # --------------------------------------------------

    # حذف فاصله قبل علائم
    text = re.sub(r"[ \t]+([.!؟،,:؛])", r"\1", text)

    # فاصله بعد علائم
    text = re.sub(
        r"([.!؟،,:؛])([^\s\n])",
        r"\1 \2",
        text
    )

    # چند نقطه اضافی
    text = re.sub(r"\.{2,}", ".", text)

    # --------------------------------------------------
    # دیالوگ‌های داستانی
    # --------------------------------------------------

    text = re.sub(
        r"(گفت|پرسید|جواب داد|لبخند زد)\s+",
        r"\1: ",
        text
    )

    # --------------------------------------------------
    # اصلاح پایان جمله‌ها
    # --------------------------------------------------

    sentence_patterns = [

        (
            r"تنهاست\s+یک روز",
            "تنهاست.\n\nیک روز"
        ),

        (
            r"نگاه می‌کردند\s+پیام",
            "نگاه می‌کردند.\n\nپیام"
        ),

        (
            r"می‌سازند\s+می‌",
            "می‌سازند.\nمی‌"
        ),

        (
            r"می‌شوی\s+می‌",
            "می‌شوی.\nمی‌"
        ),

        (
            r"کافی نیست\?",
            "کافی نیست؟"
        ),
    ]

    for pattern, repl in sentence_patterns:
        text = re.sub(pattern, repl, text)

    # --------------------------------------------------
    # عنوان‌ها
    # --------------------------------------------------

    text = re.sub(
        r"(?m)^\s*#([^\s])",
        r"# \1",
        text
    )

    # پیام → عنوان جدا
    text = re.sub(
        r"\bپیام\s+",
        "\n\nپیام:\n",
        text
    )
    # --------------------------------------------------
    # فاصله‌های اضافی
    # --------------------------------------------------

    text = re.sub(r"[ \t]+", " ", text)

    text = re.sub(
        r" *\n *",
        "\n",
        text
    )

    text = re.sub(
        r"\n{3,}",
        "\n\n",
        text
    )

    # --------------------------------------------------
    # حذف خطوط خیلی نویزی
    # --------------------------------------------------

    cleaned_lines = []

    for line in text.splitlines():

        stripped = line.strip()

        if not stripped:
            cleaned_lines.append("")
            continue

        symbols = len(
            re.findall(r'[@#$%^&*_=+<>\\/|~`]', stripped)
        )

        if len(stripped) > 0:
            ratio = symbols / len(stripped)

            if ratio > 0.4:
                continue

        cleaned_lines.append(stripped)

    text = "\n".join(cleaned_lines)

    # --------------------------------------------------
    # تمیزکاری نهایی
    # --------------------------------------------------

    text = re.sub(r"\n{3,}", "\n\n", text)

    # اصلاح نمایش RTL هر خط
    fixed_lines = []

    for line in text.splitlines():
        fixed_lines.append(rtl_display_line(line))

    text = "\n".join(fixed_lines)

    return text.strip()


def advanced_score(text):

    persian = len(
        re.findall(r'[\u0600-\u06FF]', text)
    )

    english = len(
        re.findall(r'[A-Za-z]', text)
    )

    digits = len(
        re.findall(r'\d', text)
    )

    garbage = len(
        re.findall(r'[@#$%^&*_=+<>\\/|]', text)
    )

    words = len(text.split())

    # وزن‌دهی هوشمند بر اساس زبان غالب
    if english > persian:
        english_weight = 4
        persian_weight = 1
    else:
        persian_weight = 4
        english_weight = 2

    score = (
        persian * persian_weight +
        english * english_weight +
        digits -
        garbage * 6 +
        words * 1.5
    )

    return score

