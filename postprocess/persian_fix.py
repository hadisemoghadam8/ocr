# postprocess/persian_fix.py
import re
from postprocess.rtl import smart_direction_fix


def improve_persian_text(text: str) -> str:
    """
    پست‌پروسس کامل و هوشمند برای متن فارسی خروجی OCR.
    شامل اصلاح حروف عربی→فارسی، نیم‌فاصله، علائم نگارشی، ساختار جملات و دیالوگ‌ها.
    """
    
    if not text:
        return ""

    # --------------------------------------------------
    # ۱. نرمال‌سازی اولیه
    # --------------------------------------------------
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    
    # حذف کاراکترهای نامرئی مزاحم (Zero-width, RTL controls, etc.)
    text = re.sub(
        r'[\u200c\u200d\u200e\u200f\u202a-\u202e\u2066-\u2069]',
        '',
        text
    )

    # --------------------------------------------------
    # ۲. تبدیل حروف عربی OCR به فارسی استاندارد
    # --------------------------------------------------
    arabic_to_persian = {
        "ك": "ک",
        "ي": "ی",
        "ى": "ی",
        "ئ": "ی",
        "ؤ": "و",
        "ة": "ه",
        "ۀ": "ه",
        # اصلاحات ترکیبی رایج در OCR
        "كی": "کی",
        "مى": "می",
        "نمى": "نمی",
        # کلمات عربی → فارسی
        "كافى": "کافی",
        "دنيا": "دنیا",
        # حروف اشتباه OCR
        "ھ": "ه",
    }
    for old, new in arabic_to_persian.items():
        text = text.replace(old, new)

    # --------------------------------------------------
    # ۳. اصلاح کلمات چسبیده رایج
    # --------------------------------------------------
    glued_fixes = [
        (r"\bوبه\b", "و به"),
        (r"\bواز\b", "و از"),
        (r"\bودر\b", "و در"),
        (r"\bوگفت\b", "و گفت"),
        (r"\bردشد\b", "رد شد"),
        (r"\bاماکه\b", "اما که"),
        (r"\bازآن\b", "از آن"),
        (r"\bهرروز\b", "هر روز"),
        (r"\bهیچکس\b", "هیچ‌کس"),
        (r"\bهیچکدام\b", "هیچ‌کدام"),
        (r"\bهمدیگر\b", "هم‌دیگر"),
        (r"\bهمیشهگی\b", "همیشگی"),
        # اصلاحات خاص OCR
        (r"\bپنجر\s*5\b", "پنجره"),
        (r"\bپنجر\b", "پنجره"),
        (r"\bپنجر\s*د‌ای\b", "پنجره‌ای"),
        (r"\bلحظههاست\b", "لحظه‌هاست"),
    ]
    for pattern, repl in glued_fixes:
        text = re.sub(pattern, repl, text)

    # --------------------------------------------------
    # ۴. اصلاح نیم‌فاصله برای می / نمی
    # --------------------------------------------------
    text = re.sub(r"\bمی\s*([اآ-ی]+)", r"می‌\1", text)
    text = re.sub(r"\bنمی\s*([اآ-ی]+)", r"نمی‌\1", text)
    text = re.sub(r"\bمى\s*([اآ-ی]+)", r"می‌\1", text)
    text = re.sub(r"\bنمى\s*([اآ-ی]+)", r"نمی‌\1", text)

    # --------------------------------------------------
    # ۵. اصلاح جمع‌های فارسی و پسوندها
    # --------------------------------------------------
    # لذت ها → لذت‌ها
    text = re.sub(r"\b([اآ-ی]+)\s+ها\b", r"\1‌ها", text)
    # همسایهها / خانهها → همسایه‌ها / خانه‌ها
    text = re.sub(r"\b([اآ-ی]+ه)ها\b", r"\1‌ها", text)
    # گنجشکها / فصلها → گنجشک‌ها / فصل‌ها
    text = re.sub(r"\b([اآ-ی]+[^ه‌\s])ها\b", r"\1‌ها", text)
    
    # حذف نیم‌فاصلهٔ تکراری: همسایه‌‌ها → همسایه‌ها
    text = re.sub(r"‌{2,}", "‌", text)
    
    # اصلاح‌های خاص
    text = re.sub(r"([اآ-ی]+)هه\b", r"\1ه", text)
    text = re.sub(r"هه‌های\b", r"ه‌های", text)
    text = re.sub(r"\bوبه([اآ-ی]+)", r"و به \1", text)
    text = re.sub(r"\bودر([اآ-ی]+)", r"و در \1", text)
    text = re.sub(r"\bواز([اآ-ی]+)", r"و از \1", text)
    text = re.sub(r"\b([اآ-ی]{3,})های\b", r"\1ه‌ای", text)

    # --------------------------------------------------
    # ۶. اصلاح ترین / تر / های فارسی
    # --------------------------------------------------
    text = re.sub(r"\b([اآ-ی]{2,})ترین\b", r"\1‌ترین", text)
    text = re.sub(r"\b([اآ-ی]{3,})تر\b", r"\1‌تر", text)
    text = re.sub(r"\b([اآ-ی]+ه)\s+های\b", r"\1‌های", text)
    text = re.sub(r"\b([اآ-ی]+[^ه\s])\s+های\b", r"\1‌های", text)
    text = re.sub(r"\b([اآ-ی]{3,})\s+ای\b", r"\1‌ای", text)

    # --------------------------------------------------
    # ۷. جداکردن افعال چسبیده
    # --------------------------------------------------
    text = re.sub(r"\b([اآ-ی]{2,})کرد\b", r"\1 کرد", text)
    text = re.sub(r"\b([اآ-ی]{2,})شد\b", r"\1 شد", text)
    text = re.sub(r"\b([اآ-ی]{2,})گفت\b", r"\1 گفت", text)

    # --------------------------------------------------
    # ۸. اصلاح برخی OCRهای رایج فارسی
    # --------------------------------------------------
    common_ocr_fixes = {
        "تن‌هاست": "تنهاست",
        "مي": "می",
        "نمي": "نمی",
        "كرد": "کرد",
        "ميشود": "می‌شود",
        "نمیشود": "نمی‌شود",
        "ميكند": "می‌کند",
        "نميكند": "نمی‌کند",
        "ميخواست": "می‌خواست",
        "نميدانست": "نمی‌دانست",
    }
    for old, new in common_ocr_fixes.items():
        text = text.replace(old, new)

    # --------------------------------------------------
    # ۹. اصلاح علائم نگارشی
    # --------------------------------------------------
    # حذف فاصله قبل از علائم
    text = re.sub(r"[ \t]+([.!؟،,:؛])", r"\1", text)
    # افزودن فاصله بعد از علائم (اگر کاراکتر بعدی فاصله نباشد)
    text = re.sub(r"([.!؟،,:؛])([^\s\n])", r"\1 \2", text)
    # تبدیل چند نقطه به یک نقطه
    text = re.sub(r"\.{2,}", ".", text)

    # --------------------------------------------------
    # ۱۰. دیالوگ‌های داستانی
    # --------------------------------------------------
    text = re.sub(
        r"(گفت|پرسید|جواب داد|لبخند زد)\s+",
        r"\1: ",
        text
    )

    # --------------------------------------------------
    # ۱۱. اصلاح پایان جمله‌ها (الگوهای خاص)
    # --------------------------------------------------
    sentence_patterns = [
        (r"تنهاست\s+یک روز", "تنهاست.\n\nیک روز"),
        (r"نگاه می‌کردند\s+پیام", "نگاه می‌کردند.\n\nپیام"),
        (r"می‌سازند\s+می‌", "می‌سازند.\nمی‌"),
        (r"می‌شوی\s+می‌", "می‌شوی.\nمی‌"),
        (r"کافی نیست\?", "کافی نیست؟"),
    ]
    for pattern, repl in sentence_patterns:
        text = re.sub(pattern, repl, text)

    # --------------------------------------------------
    # ۱۲. عنوان‌ها و فاصله‌های اضافی
    # --------------------------------------------------
    # اصلاح عنوان‌های مارک‌داونی: "#عنوان" → "# عنوان"
    text = re.sub(r"(?m)^\s*#([^\s])", r"# \1", text)
    # جداکردن کلمه "پیام" به عنوان یک بخش مجزا
    text = re.sub(r"\bپیام\s+", "\n\nپیام:\n", text)
    # فشرده‌سازی فاصله‌های افقی
    text = re.sub(r"[ \t]+", " ", text)
    # حذف فاصله‌های اضافی اطراف خطوط
    text = re.sub(r" *\n *", "\n", text)
    # حداکثر دو خط خالی پشت سر هم
    text = re.sub(r"\n{3,}", "\n\n", text)

    # --------------------------------------------------
    # ۱۳. حذف خطوط خیلی نویزی
    # --------------------------------------------------
    cleaned_lines = []
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped:
            cleaned_lines.append("")
            continue
        symbols = len(re.findall(r'[@#$%^&*_=+<>\\/|~`]', stripped))
        if len(stripped) > 0:
            ratio = symbols / len(stripped)
            if ratio > 0.4:
                continue
        cleaned_lines.append(stripped)
    
    text = "\n".join(cleaned_lines)
    text = re.sub(r"\n{3,}", "\n\n", text)

    # --------------------------------------------------
    # ۱۴. اصلاح نمایش RTL هر خط
    # --------------------------------------------------
    fixed_lines = [smart_direction_fix(line) for line in text.splitlines()]
    return "\n".join(fixed_lines).strip()


def fix_safe_ocr_artifacts(text: str) -> str:
    """
    اصلاحات فوق‌العاده محافظه‌کارانه برای خطاهای شناخته‌شده OCR.
    🔒 فقط روی الگوهای دقیق عمل می‌کند. هیچ تغییری در متن‌های استاندارد ایجاد نمی‌کند.
    """
    lines = text.split('\n')
    cleaned = []

    for line in lines:
        line = line.strip()
        if not line:
            cleaned.append('')
            continue

        # ۱. اصلاح چسبیدگی "که" به افعال (با پشتیبانی از فاصله و نیم‌فاصله)
        # مثال: می‌خواهدکه | میخواهدکه | می‌خواهد‌که → می‌خواهد که
        line = re.sub(
            r'(می‌خواهد|می‌کند|می‌شود|می‌گوید|خواهد|باید)[\s\u200c]*که',
            r'\1 که',
            line
        )

        # ۲. اصلاح غلط‌های خاص OCR (فقط وقتی دقیقاً همین رشته وجود داشته باشد)
        if 'انصادالله' in line:
            line = line.replace('انصادالله', 'انصارالله')
        if 'دوز معلم' in line:
            line = line.replace('دوز معلم', 'روز معلم')

        # ۳. اصلاح خط تیره اشتباه (تبدیل "د -" به "- ")
        line = line.replace('د -', '- ')

        # ۴. حذف نویزهای غیرمعتبر انتهای خط (بدون حذف اعداد یا حروف معتبر)
        line = re.sub(r"['\"\\|`{}\[\]]+$", '', line)

        cleaned.append(line)

    return '\n'.join(cleaned)


def advanced_score(text):
    """
    امتیازدهی هوشمند برای انتخاب بهترین خروجی OCR بین Tesseract و EasyOCR.
    وزن‌دهی بر اساس زبان غالب متن + جریمه برای کاراکترهای زباله.
    """
    persian = len(re.findall(r'[\u0600-\u06FF]', text))
    english = len(re.findall(r'[A-Za-z]', text))
    digits = len(re.findall(r'\d', text))
    garbage = len(re.findall(r'[@#$%^&*_=+<>\\/|]', text))
    words = len(text.split())

    # وزن‌دهی هوشمند بر اساس زبان غالب
    if english > persian:
        english_weight = 4
        persian_weight = 1
    else:
        persian_weight = 4
        english_weight = 2

    score = (
        persian * persian_weight +
        english * english_weight +
        digits -
        garbage * 6 +
        words * 1.5
    )

    return score