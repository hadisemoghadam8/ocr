"""
RTL / BiDi utilities for Persian OCR pipeline
---------------------------------------------

وظیفه این فایل:
- تمیز کردن کاراکترهای مخفی BiDi
- اصلاح فاصله‌گذاری علائم نگارشی
- جلوگیری از خراب شدن URL و @username
- اعمال RTL فقط روی خطوط واقعاً فارسی
- جلوگیری از شکستن خطوط mixed فارسی/انگلیسی

هدف:
نمایش پایدار متن فارسی در:
- txt
- terminal
- word
- markdown
- editors
"""

import re


# =========================================================
# Regex patterns
# =========================================================

# تشخیص URL
# مثال:
# https://google.com
# www.google.com
# example.ir
URL_PATTERN = re.compile(
    r'(https?://\S+|www\.\S+|[A-Za-z0-9_-]+\.(com|ir|org|net))'
)


# تشخیص username
# مثال:
# @openai
# @user_123
USERNAME_PATTERN = re.compile(
    r'@\w+'
)


# تشخیص hashtag
# مثال:
# #python
# #هوش_مصنوعی
HASHTAG_PATTERN = re.compile(
    r'#\w+'
)


# =========================================================
# Detect mixed Persian + English lines
# =========================================================

def is_mixed_line(line: str) -> bool:
    """
    بررسی می‌کند آیا خط شامل
    هم فارسی و هم انگلیسی هست یا نه.

    چرا مهم است؟
    چون خطوط mixed معمولاً اگر RTL شوند
    ترتیبشان خراب می‌شود.

    مثال:
    سلام OpenAI
    Version 2 سلام

    """

    # شمارش کاراکترهای فارسی
    persian_chars = len(
        re.findall(
            r'[\u0600-\u06FF]',
            line
        )
    )

    # شمارش کاراکترهای انگلیسی
    english_chars = len(
        re.findall(
            r'[A-Za-z]',
            line
        )
    )

    # اگر هر دو وجود داشتند → mixed
    return (
        persian_chars > 0 and
        english_chars > 0
    )


# =========================================================
# Remove hidden bidi characters
# =========================================================

def clean_bidi(text: str) -> str:
    """
    حذف کاراکترهای مخفی BiDi.

    این کاراکترها معمولاً توسط:
    - PDF
    - Word
    - Telegram
    - مرورگر
    تولید می‌شوند.

    اگر حذف نشوند:
    - ترتیب متن به‌هم می‌ریزد
    - علائم جابه‌جا می‌شوند
    - RTL/LTR خراب می‌شود
    """

    return re.sub(

        # محدوده کاراکترهای bidi
        r'[\u200e\u200f\u202a-\u202e\u2066-\u2069]',

        # حذف کامل
        '',

        text
    )


# =========================================================
# Fix punctuation spacing
# =========================================================

def fix_bidi_punctuation(text: str) -> str:
    """
    اصلاح فاصله‌گذاری علائم نگارشی.

    مثال:
    سلام ، دنیا
    ->
    سلام، دنیا

    """

    # -----------------------------------------------------
    # حذف فاصله قبل از علائم
    # -----------------------------------------------------

    text = re.sub(

        # فاصله قبل از علائم
        r'\s+([،,:؛.!؟])',

        # حذف فاصله
        r'\1',

        text
    )

    # -----------------------------------------------------
    # اضافه کردن فاصله بعد از علائم
    # -----------------------------------------------------

    text = re.sub(

        # علامت + کاراکتر بعدی بدون فاصله
        r'([،,:؛.!؟])([^\s])',

        # فاصله بعد از علامت
        r'\1 \2',

        text
    )

    # -----------------------------------------------------
    # حذف فاصله بعد از #
    # مثال:
    # hello
    # -> #hello
    # -----------------------------------------------------

    text = re.sub(

        r'#\s+',

        '#',

        text
    )

    return text


# =========================================================
# Main smart RTL fixer
# =========================================================

def smart_direction_fix(line: str) -> str:
    """
    اعمال هوشمند RTL فقط روی خطوط مناسب.

    هدف:
    - فارسی RTL شود
    - انگلیسی خراب نشود
    - URL نشکند
    - mixed text آسیب نبیند
    """

    # -----------------------------------------------------
    # حذف فاصله ابتدا و انتهای خط
    # -----------------------------------------------------

    line = line.strip()

    # اگر خط خالی بود
    if not line:
        return line

    # -----------------------------------------------------
    # پاکسازی علائم نگارشی
    # -----------------------------------------------------

    line = fix_bidi_punctuation(line)

    # -----------------------------------------------------
    # اگر URL داشت RTL نکن
    # چون لینک‌ها خراب می‌شوند
    # -----------------------------------------------------

    if URL_PATTERN.search(line):
        return line

    # -----------------------------------------------------
    # اگر username یا hashtag داشت
    # RTL نکن
    # -----------------------------------------------------

    if (
        USERNAME_PATTERN.search(line) or
        HASHTAG_PATTERN.search(line)
    ):
        return line

    # -----------------------------------------------------
    # اگر خط mixed بود RTL نکن
    # -----------------------------------------------------

    if is_mixed_line(line):
        return line

    # -----------------------------------------------------
    # شمارش فارسی
    # -----------------------------------------------------

    persian_chars = len(
        re.findall(
            r'[\u0600-\u06FF]',
            line
        )
    )

    # -----------------------------------------------------
    # شمارش انگلیسی
    # -----------------------------------------------------

    english_chars = len(
        re.findall(
            r'[A-Za-z]',
            line
        )
    )

    # -----------------------------------------------------
    # فقط اگر فارسی غالب بود
    # RTL اعمال شود
    # -----------------------------------------------------

    if persian_chars > english_chars * 2:

        # RLE = Right-To-Left Embedding
        rtl_start = '\u202B'

        # PDF = Pop Directional Formatting
        rtl_end = '\u202C'

        return rtl_start + line + rtl_end

    # -----------------------------------------------------
    # در غیر این صورت بدون تغییر
    # -----------------------------------------------------

    return line